    private void salvarPacienteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarPacienteBtnActionPerformed
       PacienteModel paciente = new PacienteModel();
        PacienteDAO pacienteDao = new PacienteDAO();
        paciente.setNome(nomeTxt.getText());
        paciente.setCpf(cpfTxt.getText());
        paciente.setEndereco(enderecoTxt.getText());
        paciente.setRg(rgTxt.getText());
        paciente.setSexo(sexoTxt.getText());
        paciente.setTelefone(telefoneTxt.getText());
        paciente.setDataNascimento(dataTxt.getText());
        paciente.setMedicoResponsavel(medicoTxt.getText());
        
      
        
        pacienteDao.salvarPaciente(paciente);
         JOptionPane.showMessageDialog(null, null, "Salvo com sucesso!", WIDTH, null);
    }//GEN-LAST:event_salvarPacienteBtnActionPerformed

