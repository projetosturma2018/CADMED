    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       PacienteDAO pacienteDAO = new PacienteDAO();

        List<PacienteModel> lista;
        lista = pacienteDAO.listarTodosPaciente();

        DefaultTableModel modeloPadrao = (DefaultTableModel) TabelaPaciente.getModel();
        for (PacienteModel paciente : lista) {
            modeloPadrao.addRow(new Object[]{
                paciente.getIdPaciente(),
                paciente.getNome(),
                paciente.getCpf(),
                paciente.getEndereco(),
                paciente.getRg(),
                paciente.getSexo(),
                paciente.getTelefone(),
                paciente.getDataNascimento(),
                paciente.getMedicoResponsavel()
            });
        }
    }//GEN-LAST:event_jButton2ActionPerformed

